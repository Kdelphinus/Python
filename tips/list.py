"""zip 코드"""
name = ["a", "b"]
value = [1, 2]

# 여러 개의 리스트에서 값을 가져오는 코드
for n, v in zip(name, value):
    print(n, v)

# a, 1
# b, 2

# 같은 열끼리 묶어서 출력
matrix = [[5, 4, 3], [2, 4, 1], [3, 1, 1]]
for m_col in zip(*matrix):
    print(m_col)

# (5, 2, 3)
# (4, 4, 1)
# (3, 1, 1)

