"""행과 열 바꾸기"""
scores = [[10, 20, 30], [60, 70, 90]]
for score in scores:
    print(*score)
# 10 20 30
# 60 70 90

scores = list(map(list, zip(*scores)))
for score in scores:
    print(*score)
# 10 60
# 20 70
# 30 90

