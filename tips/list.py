"""이중 배열일 때 set을 이용한 중복 제거"""
a = [[1, 2], [2, 3], [1, 2], [4, 5], [6, 7]]

print(set(a))  # type error

# 안의 배열은 tuple이 됨을 유의할 것
a_copy = list(set(map(tuple, a)))
print(a_copy)  # [(2, 3), (6, 7), (4, 5), (1, 2)]

# 안쪽까지 list로 바꾸기
print(list(map(list, a_copy)))  # [[2, 3], [6, 7], [4, 5], [1, 2]]

# 이중 배열 안 쪽이 tuple이라면 바로 set으로 변환 가능
b = [(1, 2), (2, 3), (1, 2), (4, 5), (6, 7)]

print(list(set(b)))  # [(2, 3), (6, 7), (4, 5), (1, 2)]

